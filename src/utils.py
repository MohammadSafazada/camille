def train_and_evaluate(model, criterion, optimizer, train_loader, test_loader, epochs):
    for epoch in range(epochs):
        model.train()
        for batch_idx, (data, target) in enumerate(train_loader):
            optimizer.zero_grad()
            output = model(data)
            loss = criterion(output, target)
            loss.backward()
            optimizer.step()

        model.eval()
        all_preds = []
        all_targets = []
        with torch.no_grad():
            for data, target in test_loader:
                output = model(data)
                pred = output.argmax(dim=1, keepdim=True)
                all_preds.extend(pred.cpu().numpy())
                all_targets.extend(target.cpu().numpy())

        all_preds = np.array(all_preds).flatten()
        all_targets = np.array(all_targets).flatten()
        macrof1 = macrof1_fn(all_preds, all_targets)
        accuracy = accuracy_fn(all_preds, all_targets)
        
        print(f'Epoch: {epoch}, Accuracy: {accuracy:.2f}%, Macro F1: {macrof1:.4f}')